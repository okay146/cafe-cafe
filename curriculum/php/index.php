    function header_motion(){
        header.classList.remove('color');
        let header_height = window.scrollY;
            if(header_height<60){
                header.classList.remove('header_motion');
                // header.style.backgroundColor='red';
            }else if(header_height>90){
                header.classList.add('header_motion');
                // top_button.style.opacity="1";
            }else{
                header.classList.remove('header_motion');
                top_button.classList.remove('none');
            }
    }

